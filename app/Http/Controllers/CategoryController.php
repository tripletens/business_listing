     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'title' => 'required|unique:category|max:255',
        ]);
        $title = $request->input('title');
        $new_category = Category::create(['title' => $title]);

        if($new_category){
            return redirect()->back()->with(['success' => 'Category created Sucessfully']);
        } else {
            return redirect()->back()->with(['error' => 'Category could not be created, Try again Later']);
        }
    }

